'use client';

import React, { useEffect, useState } from 'react';
import { useDispatch, useSelector } from 'react-redux';
import { fetchMyDownloads } from '@/redux/downloadSlice';
import Link from 'next/link';
import {
    FiCode, FiDownload, FiExternalLink, FiClock, FiShield,
    FiCpu, FiMonitor, FiRefreshCw, FiSearch, FiGrid, FiList,
    FiArrowRight, FiPackage, FiCheck
} from 'react-icons/fi';
import { useTheme } from '@/providers/ThemeProvider';

export default function MySoftwaresPage() {
    const { isDark } = useTheme();
    const dispatch = useDispatch();
    const { downloads, loading } = useSelector((state) => state.download);
    const [searchTerm, setSearchTerm] = useState('');
    const [viewMode, setViewMode] = useState('grid');

    useEffect(() => {
        dispatch(fetchMyDownloads());
    }, [dispatch]);

    const handleRefresh = () => {
        dispatch(fetchMyDownloads());
    };

    const softwares = downloads.filter(d => d.productType === 'software');

    // Filter by search
    const filteredSoftwares = softwares.filter(s =>
        s.productTitle?.toLowerCase().includes(searchTerm.toLowerCase())
    );

    // Stats
    const stats = {
        total: softwares.length,
        active: softwares.filter(s => s.isActive).length,
        downloads: softwares.reduce((acc, s) => acc + (s.downloadCount || 0), 0),
    };

    const cardClass = `rounded-2xl border transition-all duration-300 ${isDark
        ? 'bg-slate-800/50 border-white/5 hover:border-[#41bfb8]/20'
        : 'bg-white border-slate-200/60 shadow-[0_2px_10px_-4px_rgba(0,0,0,0.05)] hover:shadow-md'
        }`;

    if (loading) {
        return (
            <div className="space-y-6">
                <div className={`flex flex-col sm:flex-row sm:items-center sm:justify-between gap-4 p-5 ${cardClass}`}>
                    <div className="flex items-center gap-4">
                        <div className={`w-12 h-12 rounded-xl ${isDark ? 'bg-slate-700' : 'bg-slate-100'} animate-pulse`}></div>
                        <div className="space-y-2">
                            <div className={`h-4 w-32 rounded ${isDark ? 'bg-slate-700' : 'bg-slate-100'} animate-pulse`}></div>
                            <div className={`h-3 w-48 rounded ${isDark ? 'bg-slate-700' : 'bg-slate-100'} animate-pulse`}></div>
                        </div>
                    </div>
                </div>
                <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-4">
                    {[1, 2, 3].map((i) => (
                        <div key={i} className={cardClass}>
                            <div className={`h-40 ${isDark ? 'bg-slate-700' : 'bg-slate-100'} animate-pulse`}></div>
                            <div className="p-5 space-y-3">
                                <div className={`h-4 rounded w-3/4 ${isDark ? 'bg-slate-700' : 'bg-slate-100'} animate-pulse`}></div>
                                <div className={`h-3 rounded w-1/2 ${isDark ? 'bg-slate-700' : 'bg-slate-100'} animate-pulse`}></div>
                            </div>
                        </div>
                    ))}
                </div>
            </div>
        );
    }

    return (
        <div className="space-y-6">
            {/* Professional Compact Header */}
            <div className={`flex flex-col sm:flex-row sm:items-center sm:justify-between gap-4 p-5 ${cardClass}`}>
                <div className="flex items-center gap-4">
                    <div className="w-12 h-12 rounded-xl bg-gradient-to-br from-[#f79952] to-[#fb923c] flex items-center justify-center text-white shadow-md shadow-[#f79952]/10">
                        <FiCode size={24} />
                    </div>
                    <div>
                        <h1 className={`text-xl font-bold ${isDark ? 'text-white' : 'text-slate-800'}`}>
                            My Softwares
                        </h1>
                        <p className={`text-sm ${isDark ? 'text-slate-400' : 'text-slate-500'}`}>
                            Download and manage your purchased software
                        </p>
                    </div>
                </div>
                <div className="flex items-center gap-2">
                    <button
                        onClick={handleRefresh}
                        className={`flex items-center gap-2 px-4 py-2 rounded-xl text-sm font-bold transition-all ${isDark
                            ? 'bg-slate-700 text-slate-200 hover:bg-slate-600'
                            : 'bg-slate-100 text-slate-600 hover:bg-slate-200'
                            }`}
                    >
                        <FiRefreshCw size={16} />
                        Sync
                    </button>
                    <Link
                        href="/software"
                        className="flex items-center gap-2 px-4 py-2 bg-gradient-to-r from-[#f79952] to-[#fb923c] text-white rounded-xl text-sm font-bold shadow-md shadow-[#f79952]/10 hover:scale-105 transition-all"
                    >
                        <FiPackage size={16} />
                        Browse More
                    </Link>
                </div>
            </div>

            {/* Stats Cards */}
            <div className="grid grid-cols-1 sm:grid-cols-3 gap-4">
                <div className={`${cardClass} p-5 relative group overflow-hidden`}>
                    <div className="relative z-10 flex items-start justify-between">
                        <div>
                            <p className={`text-[10px] font-bold uppercase tracking-wider ${isDark ? 'text-slate-500' : 'text-slate-400'}`}>
                                Total Software
                            </p>
                            <h3 className={`text-3xl font-bold mt-1 ${isDark ? 'text-white' : 'text-slate-800'}`}>
                                {stats.total.toString().padStart(2, '0')}
                            </h3>
                        </div>
                        <div className="w-12 h-12 rounded-2xl bg-gradient-to-br from-[#f79952] to-[#fb923c] flex items-center justify-center text-white shadow-md group-hover:scale-110 transition-transform">
                            <FiCode size={20} />
                        </div>
                    </div>
                    <div className={`absolute bottom-0 left-0 h-1 bg-gradient-to-r from-[#f79952] to-[#fb923c] transition-all duration-300 group-hover:w-full w-0`} />
                </div>

                <div className={`${cardClass} p-5 relative group overflow-hidden`}>
                    <div className="relative z-10 flex items-start justify-between">
                        <div>
                            <p className={`text-[10px] font-bold uppercase tracking-wider ${isDark ? 'text-slate-500' : 'text-slate-400'}`}>
                                Active Licenses
                            </p>
                            <h3 className={`text-3xl font-bold mt-1 ${isDark ? 'text-white' : 'text-slate-800'}`}>
                                {stats.active.toString().padStart(2, '0')}
                            </h3>
                        </div>
                        <div className="w-12 h-12 rounded-2xl bg-gradient-to-br from-emerald-500 to-teal-500 flex items-center justify-center text-white shadow-md group-hover:scale-110 transition-transform">
                            <FiCheck size={20} />
                        </div>
                    </div>
                    <div className={`absolute bottom-0 left-0 h-1 bg-gradient-to-r from-emerald-500 to-teal-500 transition-all duration-300 group-hover:w-full w-0`} />
                </div>

                <div className={`${cardClass} p-5 relative group overflow-hidden`}>
                    <div className="relative z-10 flex items-start justify-between">
                        <div>
                            <p className={`text-[10px] font-bold uppercase tracking-wider ${isDark ? 'text-slate-500' : 'text-slate-400'}`}>
                                Total Downloads
                            </p>
                            <h3 className={`text-3xl font-bold mt-1 ${isDark ? 'text-white' : 'text-slate-800'}`}>
                                {stats.downloads.toString().padStart(2, '0')}
                            </h3>
                        </div>
                        <div className="w-12 h-12 rounded-2xl bg-gradient-to-br from-[#41bfb8] to-[#2dd4bf] flex items-center justify-center text-white shadow-md group-hover:scale-110 transition-transform">
                            <FiDownload size={20} />
                        </div>
                    </div>
                    <div className={`absolute bottom-0 left-0 h-1 bg-gradient-to-r from-[#41bfb8] to-[#2dd4bf] transition-all duration-300 group-hover:w-full w-0`} />
                </div>
            </div>

            {/* Search Bar */}
            <div className={`flex flex-col md:flex-row md:items-center gap-4 p-4 ${cardClass}`}>
                <div className="relative flex-1">
                    <FiSearch className={`absolute left-4 top-1/2 -translate-y-1/2 ${isDark ? 'text-slate-500' : 'text-slate-400'}`} />
                    <input
                        placeholder="Search software..."
                        value={searchTerm}
                        onChange={(e) => setSearchTerm(e.target.value)}
                        className={`w-full pl-11 pr-4 py-2.5 rounded-xl text-sm transition-all focus:outline-none focus:ring-2 ${isDark
                            ? 'bg-slate-800/50 border-white/5 text-slate-200 focus:ring-[#f79952]/30'
                            : 'bg-slate-50 border-slate-200 text-slate-700 focus:ring-[#f79952]/20'
                            }`}
                    />
                </div>
                <div className={`flex items-center gap-1 p-1 rounded-lg ${isDark ? 'bg-slate-800' : 'bg-slate-100'}`}>
                    <button
                        onClick={() => setViewMode('grid')}
                        className={`p-2 rounded-md transition-all ${viewMode === 'grid'
                            ? isDark ? 'bg-slate-700 text-white shadow-sm' : 'bg-white shadow-sm text-slate-800'
                            : isDark ? 'text-slate-400' : 'text-slate-500'
                            }`}
                    >
                        <FiGrid size={18} />
                    </button>
                    <button
                        onClick={() => setViewMode('list')}
                        className={`p-2 rounded-md transition-all ${viewMode === 'list'
                            ? isDark ? 'bg-slate-700 text-white shadow-sm' : 'bg-white shadow-sm text-slate-800'
                            : isDark ? 'text-slate-400' : 'text-slate-500'
                            }`}
                    >
                        <FiList size={18} />
                    </button>
                </div>
            </div>

            {/* Content */}
            {filteredSoftwares.length === 0 ? (
                <div className={`py-20 text-center rounded-2xl border-2 border-dashed ${isDark
                    ? 'border-slate-700 bg-slate-900/20'
                    : 'border-slate-200 bg-slate-50/50'
                    }`}>
                    <div className={`w-20 h-20 mx-auto rounded-2xl flex items-center justify-center mb-4 ${isDark ? 'bg-slate-800' : 'bg-slate-100'
                        }`}>
                        <FiCode size={36} className={isDark ? 'text-slate-600' : 'text-slate-300'} />
                    </div>
                    <h2 className={`text-xl font-bold ${isDark ? 'text-white' : 'text-slate-800'}`}>
                        {searchTerm ? 'No Software Found' : 'No Software Purchased Yet'}
                    </h2>
                    <p className={`text-sm mt-3 max-w-sm mx-auto ${isDark ? 'text-slate-500' : 'text-slate-500'}`}>
                        {searchTerm
                            ? `No software matching "${searchTerm}".`
                            : 'Browse our software marketplace to find useful tools and applications.'
                        }
                    </p>
                    {!searchTerm && (
                        <Link
                            href="/software"
                            className="inline-flex items-center gap-2 mt-6 px-6 py-3 bg-gradient-to-r from-[#f79952] to-[#fb923c] text-white rounded-xl font-bold text-sm shadow-md shadow-[#f79952]/10 hover:scale-105 transition-all"
                        >
                            Browse Software <FiArrowRight />
                        </Link>
                    )}
                </div>
            ) : viewMode === 'grid' ? (
                <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-5">
                    {filteredSoftwares.map((item) => (
                        <div
                            key={item._id}
                            className={`group relative rounded-2xl border overflow-hidden transition-all duration-300 ${isDark
                                ? 'bg-slate-800/50 border-white/5 hover:border-[#f79952]/20'
                                : 'bg-white border-slate-200 hover:shadow-lg'
                                }`}
                        >
                            {/* Thumbnail */}
                            <div className="relative h-40 overflow-hidden">
                                <img
                                    src={item.product?.images?.[0] || 'https://images.unsplash.com/photo-1550751827-4bd374c3f58b?auto=format&fit=crop&q=80&w=400'}
                                    alt={item.productTitle}
                                    className="w-full h-full object-cover group-hover:scale-105 transition-transform duration-500"
                                />
                                <div className="absolute inset-0 bg-gradient-to-t from-black/70 via-black/20 to-transparent" />

                                {/* Version Badge */}
                                <div className="absolute top-3 right-3">
                                    <span className="px-2 py-1 rounded-lg bg-white/20 backdrop-blur-sm text-white text-[10px] font-bold">
                                        {item.product?.version || 'v1.0.0'}
                                    </span>
                                </div>

                                <div className="absolute bottom-3 left-3 right-3">
                                    <span className="inline-flex items-center gap-1 px-2.5 py-1 rounded-lg text-[10px] font-bold uppercase tracking-widest text-white bg-gradient-to-r from-[#f79952] to-[#fb923c] mb-2">
                                        <FiCode size={10} /> Software
                                    </span>
                                    <h3 className="text-white font-bold text-sm line-clamp-2 leading-snug">
                                        {item.productTitle}
                                    </h3>
                                </div>
                            </div>

                            {/* Info */}
                            <div className="p-4 space-y-4">
                                <div className="space-y-2">
                                    <div className="flex items-center justify-between text-[10px] font-bold">
                                        <span className={`uppercase tracking-widest flex items-center gap-1.5 ${isDark ? 'text-slate-500' : 'text-slate-400'}`}>
                                            <FiClock size={10} /> Purchased
                                        </span>
                                        <span className={isDark ? 'text-slate-300' : 'text-slate-600'}>
                                            {new Date(item.createdAt).toLocaleDateString()}
                                        </span>
                                    </div>
                                    <div className="flex items-center justify-between text-[10px] font-bold">
                                        <span className={`uppercase tracking-widest flex items-center gap-1.5 ${isDark ? 'text-slate-500' : 'text-slate-400'}`}>
                                            <FiMonitor size={10} /> Status
                                        </span>
                                        <span className={item.isActive ? 'text-emerald-500' : 'text-slate-400'}>
                                            {item.isActive ? 'Active' : 'Inactive'}
                                        </span>
                                    </div>
                                    <div className="flex items-center justify-between text-[10px] font-bold">
                                        <span className={`uppercase tracking-widest flex items-center gap-1.5 ${isDark ? 'text-slate-500' : 'text-slate-400'}`}>
                                            <FiShield size={10} /> License
                                        </span>
                                        <span className="text-[#41bfb8]">Universal</span>
                                    </div>
                                </div>

                                {/* Actions */}
                                <div className="flex items-center gap-2">
                                    <button
                                        onClick={() => window.open(item.product?.downloadFile, '_blank')}
                                        disabled={!item.product?.downloadFile}
                                        className="flex-1 flex items-center justify-center gap-2 px-4 py-2.5 bg-gradient-to-r from-[#f79952] to-[#fb923c] text-white rounded-xl font-bold text-xs shadow-md shadow-[#f79952]/10 hover:shadow-lg hover:-translate-y-0.5 active:translate-y-0 transition-all disabled:opacity-50"
                                    >
                                        <FiDownload size={14} /> Download
                                    </button>
                                    <button className={`p-2.5 rounded-xl transition-all ${isDark
                                        ? 'bg-slate-700 text-slate-300 hover:text-[#f79952]'
                                        : 'bg-slate-100 text-slate-500 hover:text-[#f79952]'
                                        }`}>
                                        <FiExternalLink size={16} />
                                    </button>
                                </div>
                            </div>
                        </div>
                    ))}
                </div>
            ) : (
                <div className={`rounded-2xl border overflow-hidden divide-y ${isDark ? 'bg-slate-800/50 border-white/5 divide-white/5' : 'bg-white border-slate-200 divide-slate-100'}`}>
                    {filteredSoftwares.map((item) => (
                        <div key={item._id} className={`flex items-center gap-4 p-4 transition-colors ${isDark ? 'hover:bg-slate-800' : 'hover:bg-slate-50'}`}>
                            <div className="w-16 h-12 rounded-xl overflow-hidden shrink-0 border border-slate-100 dark:border-white/5">
                                <img src={item.product?.images?.[0]} className="w-full h-full object-cover" />
                            </div>
                            <div className="flex-1 min-w-0">
                                <h3 className={`text-sm font-bold truncate ${isDark ? 'text-slate-200' : 'text-slate-800'}`}>{item.productTitle}</h3>
                                <div className="flex items-center gap-3 mt-1">
                                    <span className="text-[9px] font-bold uppercase px-1.5 py-0.5 rounded tracking-wider bg-orange-100 text-orange-600 dark:bg-orange-500/10 dark:text-orange-400">
                                        {item.product?.version || 'v1.0.0'}
                                    </span>
                                    <span className={`text-[10px] ${isDark ? 'text-slate-500' : 'text-slate-400'}`}>
                                        {new Date(item.createdAt).toLocaleDateString()}
                                    </span>
                                </div>
                            </div>
                            <button
                                onClick={() => window.open(item.product?.downloadFile, '_blank')}
                                disabled={!item.product?.downloadFile}
                                className={`p-2.5 rounded-xl transition-all ${isDark
                                    ? 'bg-slate-700 text-slate-300 hover:bg-[#f79952] hover:text-white'
                                    : 'bg-slate-100 text-slate-500 hover:bg-[#f79952] hover:text-white'
                                    } disabled:opacity-50`}
                            >
                                <FiDownload size={16} />
                            </button>
                        </div>
                    ))}
                </div>
            )}

            {/* Info Notice */}
            <div className={`p-6 rounded-2xl border flex flex-col md:flex-row items-center justify-between gap-4 transition-all ${isDark
                ? 'bg-slate-800/50 border-white/5'
                : 'bg-gradient-to-br from-slate-50 to-white border-slate-100'
                }`}>
                <div className="flex items-center gap-4 text-center md:text-left">
                    <div className={`w-12 h-12 rounded-xl flex items-center justify-center shrink-0 ${isDark
                        ? 'bg-slate-700 text-[#f79952]'
                        : 'bg-white text-[#f79952] shadow-md border border-slate-100'
                        }`}>
                        <FiCpu size={22} />
                    </div>
                    <div>
                        <h4 className={`text-base font-bold ${isDark ? 'text-white' : 'text-slate-800'}`}>Lifetime Updates Included</h4>
                        <p className={`text-xs mt-0.5 ${isDark ? 'text-slate-400' : 'text-slate-500'}`}>
                            All software purchases include free lifetime updates. Check back for new versions!
                        </p>
                    </div>
                </div>
                <Link
                    href="/dashboard/user/support"
                    className={`px-5 py-2.5 rounded-xl font-bold text-sm transition-all ${isDark
                        ? 'bg-slate-700 text-white hover:bg-slate-600'
                        : 'bg-slate-900 text-white hover:bg-slate-800'
                        }`}
                >
                    Get Support
                </Link>
            </div>
        </div>
    );
}
